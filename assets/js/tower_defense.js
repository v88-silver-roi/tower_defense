function spawnRandomWorld(){
    var randomWorld = Math.floor(Math.random() * 6);

    if(randomWorld == 2){
        world = world2;
    }
    else if(randomWorld == 3){
        world = world3;
    }
    else if(randomWorld == 4){
        world = world4;
    }
    else if(randomWorld == 5){
        world = world5;
    }
}

function displayWorld(){
    var output = '';

    for (var i=0; i<world.length; i++){
        output += "\n<tr class='row'>";
        for(var j=0; j<world[i].length; j++){
            if(world[i][j] == 0)
                output += "<td class='soil'></td>";
            else if(world[i][j] == 1)
                output += "<td class='water'></td>";
            else if(world[i][j] == 2)
                output += "<td class='grass'></td>";
        }
        output += "\n</tr>";
    }
    console.log(output);
    document.getElementById('world').innerHTML = output;
}

function gameloop(){
    displayWorld();
}

